print(2 + 2) # Adição
print(2 - 2) # Subtração
print(2 * 2) # Multiplicação
print(2 / 2) # Divisão
print(2 % 2) # Módulo (resto da divisão)
print(2 // 2)   # Divisão inteira (sem casas decimais)
print(2 ** 2)   # Exponenciação (2 elevado a 2)

print(1 + 2 * 3)   # Ordem das operações (multiplicação antes da adição)
print((1 + 2) * 3) # Uso de parênteses para alterar a ordem das operações

# Exponenciação
print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)

print(+2) # Operador unário de positivo
print(-2) # Operador unário de negativo